def is_palindrome(s):
    # Проверяем, является ли строка пустой или состоит только из одного символа
    if len(s) <= 1:
        return True

    # Сравниваем первый символ строки с последним символом
    if s[0] != s[-1]:
        return False

    # Удаляем первый и последний символы из строки
    s = s[1:-1]

    # Повторяем проверку, пока строка не станет пустой или состоит только из одного символа
    return is_palindrome(s)

# Ввод строки пользователем
s = input().strip()

# Вывод результата
if is_palindrome(s):
    print("yes")
else:
    print("no")